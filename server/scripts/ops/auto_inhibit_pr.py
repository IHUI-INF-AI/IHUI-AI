"""Phase 10 建议 3: 抑制工单自动开 PR 工具.

目的:
  把 generate_inhibit_ticket 输出的 silence 规则 YAML, 自动开 PR 到 alertmanager 配置仓库.
  走 review-merge-deploy 流程, 避免直接改生产.

PR 内容:
  - 分支名: inhibit/<date>-<incident_hash>
  - 文件: alertmanager/silences/<date>-<short_id>.yml
  - PR 标题: "[Inhibit Ticket Auto] 抑制 N 条 flapping 告警 (<date>)"
  - PR 描述: 包含抑制工单 markdown + checklist + 关联 incident_id

用法:
  # dry-run 模式 (默认, 不真开 PR, 只生成 patch 文件)
  python scripts/ops/auto_inhibit_pr.py --date 20260616 --dry-run

  # 真实模式 (用 GitHub App token)
  GITHUB_TOKEN=ghp_xxx \\
  python scripts/ops/auto_inhibit_pr.py --date 20260616 --repo zhs/alertmanager-config

输出:
  - logs/inhibit_prs/<date>.patch (dry-run 模式 patch 文件)
  - logs/inhibit_prs/<date>.pr.json (真实模式 PR 元数据)
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from datetime import UTC, datetime
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent.parent

INHIBIT_TICKET_DIR = ROOT / "logs" / "inhibit_tickets"
INHIBIT_PR_DIR = ROOT / "logs" / "inhibit_prs"

# GitHub API: PR 创建端点
# POST /repos/{owner}/{repo}/pulls
GITHUB_API_BASE = "https://api.github.com"


# ---------------------------------------------------------------------------
# 1. 读抑制工单 → 提取 silence 规则
# ---------------------------------------------------------------------------


def load_inhibit_ticket(date: str) -> dict:
    """从 logs/inhibit_tickets/<date>.json 读工单."""
    p = INHIBIT_TICKET_DIR / f"{date}.json"
    if not p.exists():
        print(f"✗ 工单不存在: {p}")
        sys.exit(2)
    return json.loads(p.read_text(encoding="utf-8"))


def extract_silence_rules(ticket: dict) -> list[dict]:
    """从工单提取 silence 规则 (alertmanager silence 格式)."""
    rules = []
    ticket_date = ticket.get("date", "unknown")
    for t in ticket.get("tickets", []):
        rules.append(
            {
                "matchers": [
                    {"name": "alertname", "value": t["alertname"], "isRegex": False},
                    {"name": "severity", "value": t["severity"], "isRegex": False},
                ],
                "startsAt": datetime.now(UTC).isoformat().replace("+00:00", "Z"),
                "endsAt": (datetime.now(UTC).timestamp() + t["inhibit_hours"] * 3600),
                "createdBy": "zhs-auto-inhibit-bot",
                "comment": (
                    f"auto-inhibit flapping_score={t['flapping_score']:.2f} "
                    f"firing={t['firing_count']} "
                    f"ticket=INC-{ticket_date}"
                ),
            }
        )
    return rules


def render_silence_yaml(rules: list[dict]) -> str:
    """构造 alertmanager silence 配置文件 (按 alertmanager 文件格式)."""
    out = [
        "# Auto-generated by scripts/ops/auto_inhibit_pr.py",
        f"# Date: {datetime.now(UTC).strftime('%Y-%m-%d %H:%M:%S UTC')}",
        f"# Ticket count: {len(rules)}",
        "silences:",
    ]
    for r in rules:
        out.append("  - matchers:")
        for m in r["matchers"]:
            out.append(f"      - name: {m['name']}")
            out.append(f"        value: \"{m['value']}\"")
            out.append(f"        isRegex: {str(m['isRegex']).lower()}")
        out.append(f"    startsAt: '{r['startsAt']}'")
        out.append(
            f"    endsAt: '{datetime.fromtimestamp(r['endsAt'], tz=UTC).isoformat().replace('+00:00', 'Z')}'"
        )
        out.append(f"    createdBy: {r['createdBy']}")
        out.append(f"    comment: \"{r['comment']}\"")
    return "\n".join(out) + "\n"


# ---------------------------------------------------------------------------
# 2. 构造 PR 内容 (标题/描述/分支名)
# ---------------------------------------------------------------------------


def make_branch_name(date: str) -> str:
    return f"inhibit/{date}-{datetime.now(UTC).strftime('%H%M%S')}"


def make_pr_title(date: str, count: int) -> str:
    return f"[Inhibit Ticket Auto] 抑制 {count} 条 flapping 告警 ({date})"


def make_pr_body(ticket: dict, rules: list[dict], date: str) -> str:
    """PR 描述: 抑制工单摘要 + checklist + 关联."""
    md = [
        "## 自动生成的抑制工单 PR",
        "",
        f"- 生成时间: `{datetime.now(UTC).isoformat()}`",
        f"- 抑制阈值: flapping_score >= {ticket['threshold']}, firing >= {ticket['min_firing']}",
        f"- 命中工单: **{ticket['ticket_count']}** 条",
        f"- 关联: 工单 JSON `{INHIBIT_TICKET_DIR}/{date}.json`",
        "",
        "## 待抑制告警",
        "",
        "| 告警 | Flapping | 触发 | 抑制时长 |",
        "|------|----------|------|----------|",
    ]
    for t in ticket.get("tickets", []):
        md.append(f"| `{t['alertname']}` | {t['flapping_score']:.2f} | {t['firing_count']} | {t['inhibit_hours']}h |")
    md.extend(
        [
            "",
            "## 部署后 24h 复盘 checklist",
            "",
            "- [ ] 检查抑制规则是否生效 (alertmanager /api/v2/silences)",
            "- [ ] 监控 flapping_score 是否下降",
            "- [ ] 走值班确认后 merge",
            "- [ ] 合并后 30 min 内检查告警频率",
            "- [ ] 24h 后回查, 若 flapping 仍 > 0.5 走 Phase 9 抑制工单调度",
            "",
            "## 回滚方案",
            "",
            "```bash",
            "# 合并后 1h 内发现抑制规则错误:",
            "git revert <merge-commit-sha>",
            "kubectl apply -f alertmanager/config.yml",
            "```",
            "",
            "---",
            "🤖 Auto-generated by `scripts/ops/auto_inhibit_pr.py`",
        ]
    )
    return "\n".join(md)


# ---------------------------------------------------------------------------
# 3. 调 GitHub API 开 PR (真实模式)
# ---------------------------------------------------------------------------


def open_github_pr(
    repo: str,
    branch: str,
    file_path: str,
    file_content: str,
    pr_title: str,
    pr_body: str,
    token: str,
    base_branch: str = "main",
) -> dict:
    """通过 GitHub API 真正开 PR.

    步骤:
      1. GET repo 信息拿 default branch SHA
      2. POST /repos/{owner}/{repo}/git/refs 创建新分支
      3. PUT /repos/{owner}/{repo}/contents/<path> 写文件
      4. POST /repos/{owner}/{repo}/pulls 开 PR
    """
    import requests

    headers = {
        "Authorization": f"token {token}",
        "Accept": "application/vnd.github.v3+json",
    }
    base = f"{GITHUB_API_BASE}/repos/{repo}"

    # 1. 拿 base 分支 SHA
    r = requests.get(f"{base}/git/ref/heads/{base_branch}", headers=headers, timeout=15)
    r.raise_for_status()
    base_sha = r.json()["object"]["sha"]

    # 2. 创建新分支
    r = requests.post(
        f"{base}/git/refs",
        json={"ref": f"refs/heads/{branch}", "sha": base_sha},
        headers=headers,
        timeout=15,
    )
    r.raise_for_status()

    # 3. 写文件
    import base64 as _b64

    r = requests.put(
        f"{base}/contents/{file_path}",
        json={
            "message": f"chore(inhibit): add silence rules for {datetime.now(UTC).strftime('%Y%m%d')}",
            "branch": branch,
            "content": _b64.b64encode(file_content.encode("utf-8")).decode("ascii"),
        },
        headers=headers,
        timeout=15,
    )
    r.raise_for_status()

    # 4. 开 PR
    r = requests.post(
        f"{base}/pulls",
        json={
            "title": pr_title,
            "head": branch,
            "base": base_branch,
            "body": pr_body,
            "maintainer_can_modify": True,
        },
        headers=headers,
        timeout=15,
    )
    r.raise_for_status()
    return r.json()


# ---------------------------------------------------------------------------
# 4. dry-run 模式: 只写 patch 文件
# ---------------------------------------------------------------------------


def dry_run_pr(
    date: str,
    rules: list[dict],
    branch: str,
    file_path: str,
    file_content: str,
    pr_title: str,
    pr_body: str,
) -> Path:
    """dry-run 模式: 生成 patch 文件 + 元数据, 不真开 PR."""
    INHIBIT_PR_DIR.mkdir(parents=True, exist_ok=True)

    # 写 patch
    patch_path = INHIBIT_PR_DIR / f"{date}.patch"
    file_lines = file_content.splitlines()
    header = (
        f"diff --git a/{file_path} b/{file_path}\n"
        "new file mode 100644\n"
        "index 0000000..1111111\n"
        "--- /dev/null\n"
        f"+++ b/{file_path}\n"
        f"@@ -0,0 +1,{len(file_lines)} @@\n"
    )
    body = "\n".join(f"+{line}" for line in file_lines)
    patch_path.write_text(header + body + "\n", encoding="utf-8")

    # 写 PR 元数据
    pr_meta = {
        "date": date,
        "branch": branch,
        "file_path": file_path,
        "pr_title": pr_title,
        "pr_body": pr_body,
        "rule_count": len(rules),
        "generated_at": datetime.now(UTC).isoformat(),
        "patch_file": str(patch_path.relative_to(ROOT)),
    }
    meta_path = INHIBIT_PR_DIR / f"{date}.pr.json"
    meta_path.write_text(json.dumps(pr_meta, ensure_ascii=False, indent=2), encoding="utf-8")

    return meta_path


# ---------------------------------------------------------------------------
# 5. 主流程
# ---------------------------------------------------------------------------


def main() -> int:
    p = argparse.ArgumentParser(description="抑制工单自动开 PR")
    p.add_argument("--date", required=True, help="演练日期 YYYYMMDD")
    p.add_argument("--repo", default="zhs/alertmanager-config", help="目标 alertmanager config 仓库")
    p.add_argument("--base-branch", default="main", help="PR 目标分支")
    p.add_argument("--dry-run", action="store_true", help="只生成 patch 文件, 不真开 PR")
    args = p.parse_args()

    # 1. 读工单
    ticket = load_inhibit_ticket(args.date)
    if ticket.get("ticket_count", 0) == 0:
        print(f"ℹ {args.date} 无待抑制告警, 跳过 PR")
        return 0
    print(f"[load] 工单 {args.date}: {ticket['ticket_count']} 条")

    # 2. 提取 silence 规则
    rules = extract_silence_rules(ticket)
    file_content = render_silence_yaml(rules)
    print(f"[rules] 提取 {len(rules)} 条 silence 规则")

    # 3. 构造 PR 内容
    branch = make_branch_name(args.date)
    file_path = f"alertmanager/silences/{args.date}.yml"
    pr_title = make_pr_title(args.date, ticket["ticket_count"])
    pr_body = make_pr_body(ticket, rules, args.date)

    # 4. dry-run 或真实开 PR
    if args.dry_run:
        meta = dry_run_pr(args.date, rules, branch, file_path, file_content, pr_title, pr_body)
        print(f"[dry-run] patch: {INHIBIT_PR_DIR / f'{args.date}.patch'}")
        print(f"[dry-run] meta:  {meta}")
        print(f"[PR 标题] {pr_title}")
        print(f"[分支] {branch}")
        return 0

    # 真实模式
    token = os.environ.get("GITHUB_TOKEN", "")
    if not token:
        print("✗ 真实模式需 GITHUB_TOKEN 环境变量")
        return 2
    try:
        pr = open_github_pr(
            repo=args.repo,
            branch=branch,
            file_path=file_path,
            file_content=file_content,
            pr_title=pr_title,
            pr_body=pr_body,
            token=token,
            base_branch=args.base_branch,
        )
        meta = {
            "date": args.date,
            "pr_number": pr["number"],
            "pr_url": pr["html_url"],
            "branch": branch,
            "rule_count": len(rules),
            "created_at": pr["created_at"],
        }
        meta_path = INHIBIT_PR_DIR / f"{args.date}.pr.json"
        meta_path.write_text(json.dumps(meta, ensure_ascii=False, indent=2), encoding="utf-8")
        print(f"[OK] PR #{pr['number']}: {pr['html_url']}")
        return 0
    except Exception as e:
        print(f"✗ 开 PR 失败: {e}")
        return 1


if __name__ == "__main__":
    sys.exit(main())
