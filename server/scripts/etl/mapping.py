"""主键映射持久化器.

把 transformer 阶段产生的 (source_table, old_id) → new_uuid 批量落库.
"""
from __future__ import annotations

import logging
from typing import Iterable

from sqlalchemy.dialects.postgresql import insert as pg_insert

from app.database import get_session
from app.models.id_mapping import IdMapping
from scripts.etl.config import MigrationTask

logger = logging.getLogger(__name__)


def persist_mappings(
    batch_id: str,
    task: MigrationTask,
    mapping_cache: dict[tuple[str, int], str],
) -> int:
    """把当前 task 产生的 id_mapping 全部写入 G 盘.

    使用 ON CONFLICT DO NOTHING 保证幂等 (重复执行不会报错).
    """
    if not mapping_cache:
        return 0
    rows = [
        {
            "id": new_uuid,  # 复用 new_uuid 做主键
            "source_table": source_table or task.source_table,
            "old_id": str(old_id),
            "new_uuid": new_uuid,
            "migration_batch": batch_id,
            "remark": f"auto-generated by {task.source_db}",
        }
        for (source_table, old_id), new_uuid in mapping_cache.items()
    ]

    with get_session() as db:
        stmt = pg_insert(IdMapping).values(rows)
        stmt = stmt.on_conflict_do_nothing(index_elements=["source_table", "old_id"])
        db.execute(stmt)
    logger.info(f"[persist_mappings] task={task.target_table} count={len(rows)}")
    return len(rows)


def lookup_mappings(task: MigrationTask) -> dict[tuple[str, int], str]:
    """从 G 盘反查已有映射, 用于断点续传 (已迁移的 H 盘行不会重复生成 UUID)."""
    cache: dict[tuple[str, int], str] = {}
    with get_session() as db:
        rows = (
            db.query(IdMapping.source_table, IdMapping.old_id, IdMapping.new_uuid)
            .filter(IdMapping.source_table == task.source_table)
            .yield_per(1000)
        )
        for source_table, old_id, new_uuid in rows:
            cache[(source_table, int(old_id))] = new_uuid
    return cache
