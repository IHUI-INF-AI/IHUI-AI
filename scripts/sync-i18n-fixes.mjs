function ensure(obj, path) {
  const parts = path.split('.')
  let cur = obj
  for (const p of parts) {
    if (!(p in cur)) cur[p] = {}
    cur = cur[p]
  }
  return cur
}

let totalAdded = 0

for (const [lang, translations] of Object.entries(langs)) {
  const filePath = join(MESSAGES_DIR, `${lang}.json`)
  const json = JSON.parse(readFileSync(filePath, 'utf8'))

  for (const [key, value] of Object.entries(translations)) {
    const ns = nsMap[key]
    if (!ns) continue
    const nsObj = ensure(json, ns)
    if (!(key in nsObj)) {
      nsObj[key] = value
      totalAdded++
    }
  }

  writeFileSync(filePath, JSON.stringify(json, null, 2) + '\n', 'utf8')
  console.log(`✓ ${lang}.json:补全完成`)
}

console.log(`\n总计补全: ${totalAdded} 个键`)
