// 读取旧架构翻译
function loadOld() {
  const files = ['en', 'ja', 'ko', 'zh-CN', 'zh-TW'];
  const result = {};
  for (const l of files) {
    const raw = fs.readFileSync(`old-${l}.json`, 'utf8');
    result[l] = JSON.parse(raw);
  }
  return result;
}

// 扁平化对象
function flat(o, p = '') {
  let r = {};
  for (const [k, v] of Object.entries(o)) {
    const k2 = p ? p + '.' + k : k;
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      Object.assign(r, flat(v, k2));
    } else {
      r[k2] = v;
    }
  }
  return r;
}

// 还原嵌套对象
function unflat(o) {
  const result = {};
  for (const [k, v] of Object.entries(o)) {
    const parts = k.split('.');
    let cur = result;
    for (let i = 0; i < parts.length - 1; i++) {
      if (!cur[parts[i]] || typeof cur[parts[i]] !== 'object') cur[parts[i]] = {};
      cur = cur[parts[i]];
    }
    cur[parts[parts.length - 1]] = v;
  }
  return result;
}

// 标准化字符串用于匹配
function norm(s) {
  return String(s || '').toLowerCase().replace(/[^a-z0-9\u4e00-\u9fff]/g, '');
}

// 键名最后一段
function lastSeg(k) {
  const parts = k.split('.');
  return parts[parts.length - 1].toLowerCase();
}

