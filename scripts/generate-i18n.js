// 主函数
function main() {
  const old = loadOld();
  const oldEn = flat(old['en']);
  const oldZhCn = flat(old['zh-CN']);
  const oldJa = flat(old['ja']);
  const oldKo = flat(old['ko']);
  const oldTw = flat(old['zh-TW']);

  const newEnRaw = JSON.parse(fs.readFileSync('apps/web/messages/en.json', 'utf8'));
  const newZhCnRaw = JSON.parse(fs.readFileSync('apps/web/messages/zh-CN.json', 'utf8'));
  const newEn = flat(newEnRaw);
  const newZhCn = flat(newZhCnRaw);

  // 建立值映射表
  const mapJa = {}, mapKo = {}, mapTw = {};
  for (const k of Object.keys(oldEn)) {
    const v = oldEn[k];
    if (typeof v === 'string' && v) {
      const nv = norm(v);
      if (!mapJa[nv] && oldJa[k]) mapJa[nv] = oldJa[k];
      if (!mapKo[nv] && oldKo[k]) mapKo[nv] = oldKo[k];
      if (!mapTw[nv] && oldTw[k]) mapTw[nv] = oldTw[k];
    }
  }
  for (const k of Object.keys(oldZhCn)) {
    const v = oldZhCn[k];
    if (typeof v === 'string' && v) {
      const nv = norm(v);
      if (!mapJa[nv] && oldJa[k]) mapJa[nv] = oldJa[k];
      if (!mapKo[nv] && oldKo[k]) mapKo[nv] = oldKo[k];
      if (!mapTw[nv] && oldTw[k]) mapTw[nv] = oldTw[k];
    }
  }

  // 建立键名映射表
  const keyMapJa = {}, keyMapKo = {}, keyMapTw = {};
  for (const k of Object.keys(oldEn)) {
    const ls = lastSeg(k);
    if (oldJa[k] && !keyMapJa[ls]) keyMapJa[ls] = oldJa[k];
    if (oldKo[k] && !keyMapKo[ls]) keyMapKo[ls] = oldKo[k];
    if (oldTw[k] && !keyMapTw[ls]) keyMapTw[ls] = oldTw[k];
  }

  // 生成翻译
  const resultJa = {}, resultKo = {}, resultTw = {};
  let statHit = 0, statDict = 0, statTw = 0, statFallback = 0;
  const fallbackKeys = [];

  for (const k of Object.keys(newEn)) {
    const enVal = newEn[k];
    const zhVal = newZhCn[k];

    if (typeof enVal !== 'string') {
      // 非字符串值直接保留
      resultJa[k] = enVal;
      resultKo[k] = enVal;
      resultTw[k] = enVal;
      continue;
    }

    const nv1 = norm(enVal);
    const nv2 = zhVal ? norm(zhVal) : '';
    const ls = lastSeg(k);

    // 1. 尝试值映射 + 键名映射
    let ja = mapJa[nv1] || mapJa[nv2] || keyMapJa[ls];
    let ko = mapKo[nv1] || mapKo[nv2] || keyMapKo[ls];
    let tw = mapTw[nv1] || mapTw[nv2] || keyMapTw[ls];

    if (ja && ko && tw) {
      statHit++;
    } else {
      // 2. 尝试翻译词典
      const dict = zhVal ? ZH_JA_KO[zhVal] : null;
      if (dict) {
        if (!ja) ja = dict.ja;
        if (!ko) ko = dict.ko;
        statDict++;
      }

      // 3. zh-TW 用简繁转换
      if (!tw && zhVal) {
        tw = s2t(zhVal);
        statTw++;
      }

      // 4. 对于仍未翻译的 ja/ko，用 zh-CN 值作为 fallback（标记）
      if (!ja) { ja = zhVal || enVal; fallbackKeys.push(k + ' (ja)'); }
      if (!ko) { ko = zhVal || enVal; fallbackKeys.push(k + ' (ko)'); }
      if (!tw) { tw = zhVal || enVal; fallbackKeys.push(k + ' (tw)'); }
    }

    resultJa[k] = ja;
    resultKo[k] = ko;
    resultTw[k] = tw;
  }

  // 写入文件
  const jaOut = unflat(resultJa);
  const koOut = unflat(resultKo);
  const twOut = unflat(resultTw);

  fs.writeFileSync('apps/web/messages/ja.json', JSON.stringify(jaOut, null, 2) + '\n', 'utf8');
  fs.writeFileSync('apps/web/messages/ko.json', JSON.stringify(koOut, null, 2) + '\n', 'utf8');
  fs.writeFileSync('apps/web/messages/zh-TW.json', JSON.stringify(twOut, null, 2) + '\n', 'utf8');

  console.log('=== i18n 翻译生成统计 ===');
  console.log('总键数:', Object.keys(newEn).length);
  console.log('旧架构映射命中:', statHit);
  console.log('翻译词典命中:', statDict);
  console.log('简繁转换:', statTw);
  console.log('Fallback:', fallbackKeys.length);
  if (fallbackKeys.length > 0) {
    console.log('Fallback keys (前20):');
    console.log(fallbackKeys.slice(0, 20).join('\n'));
  }
  console.log('输出: ja.json, ko.json, zh-TW.json');
}

main();
