/**
 * miniapp git hooks 安装脚本（跨平台 Node 实现）
 *
 * 设计要点：
 * 1. 不安装 husky 包（避免与 client 的 simple-git-hooks 冲突）
 * 2. 不修改 core.hooksPath（避免影响整个仓库的 hooks 行为）
 * 3. 直接在 .git/hooks/{pre-commit,commit-msg} 追加 miniapp 检查（用 marker 标记，可重复运行）
 * 4. 只在 miniapp 有暂存文件时运行检查（避免影响 client commit 速度）
 * 5. 如果 .git/hooks/xxx 已存在（被 simple-git-hooks 或 Git LFS 占用），追加 miniapp 检查
 *
 * 触发方式：
 * - cd client/miniapp && npm install  →  自动触发 prepare 脚本
 * - cd client/miniapp && npm run prepare  →  手动触发
 *
 * 支持的 hooks：
 * - pre-commit：运行 lint-staged（eslint + stylelint）
 * - commit-msg：运行 commitlint（conventional commits 规范校验）
 */
import * as fs from 'node:fs'
import * as nodePath from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = nodePath.dirname(__filename)

// 定位 .git 目录（miniapp -> client -> IHUI-AI -> .git）
const gitDir = nodePath.resolve(__dirname, '..', '..', '..', '.git')
const hooksDir = nodePath.join(gitDir, 'hooks')

// hook 配置：每个 hook 用独立的 MARKER 标记，支持幂等安装/卸载
const HOOKS = [
  {
    name: 'pre-commit',
    marker: 'miniapp pre-commit hook (auto-generated by @aizhs/miniapp)',
    content: `#!/bin/sh
# miniapp pre-commit hook (auto-generated by @aizhs/miniapp)
# 只在 miniapp 有暂存文件时运行 lint-staged，避免影响 client commit 速度
STAGED=$(git diff --cached --name-only --diff-filter=ACM | grep "^client/miniapp/" || true)
if [ -n "$STAGED" ]; then
  echo "[miniapp] 检测到 miniapp 暂存文件，运行 lint-staged..."
  cd client/miniapp && npx lint-staged || exit 1
fi
`,
  },
  {
    name: 'commit-msg',
    marker: 'miniapp commit-msg hook (auto-generated by @aizhs/miniapp)',
    content: `#!/bin/sh
# miniapp commit-msg hook (auto-generated by @aizhs/miniapp)
# 只在 miniapp 有暂存文件时运行 commitlint，避免影响 client commit
STAGED=$(git diff --cached --name-only --diff-filter=ACM | grep "^client/miniapp/" || true)
if [ -n "$STAGED" ]; then
  echo "[miniapp] 检测到 miniapp 暂存文件，运行 commitlint..."
  cd client/miniapp && npx --no-install commitlint --edit "$1" || exit 1
fi
`,
  },
]

function ensureHooksDir() {
  if (!fs.existsSync(hooksDir)) {
    fs.mkdirSync(hooksDir, { recursive: true })
  }
}

/**
 * 安装单个 hook：若不存在则创建；若已存在但无 MARKER 则追加；若已含 MARKER 则跳过
 */
function installHook(hookConfig) {
  const hookFile = nodePath.join(hooksDir, hookConfig.name)

  try {
    if (!fs.existsSync(hookFile)) {
      // hook 不存在，直接创建
      fs.writeFileSync(hookFile, hookConfig.content, { mode: 0o755 })
      fs.chmodSync(hookFile, 0o755)
      console.log(`✓ 已安装 miniapp ${hookConfig.name} hook 到 .git/hooks/${hookConfig.name}`)
      return
    }

    // hook 已存在，检查是否已包含 miniapp 标记
    const existing = fs.readFileSync(hookFile, 'utf-8')
    if (existing.includes(hookConfig.marker)) {
      console.log(`✓ ${hookConfig.name} hook 已包含 miniapp 检查，跳过`)
      return
    }

    // 追加 miniapp 检查到现有 hook（保留原有内容）
    const newContent = existing.trimEnd() + '\n\n' + hookConfig.content
    fs.writeFileSync(hookFile, newContent, { mode: 0o755 })
    fs.chmodSync(hookFile, 0o755)
    console.log(`✓ 已追加 miniapp 检查到现有 ${hookConfig.name} hook（保留原有内容）`)
  } catch (error) {
    console.warn(`⚠ 安装 ${hookConfig.name} hook 失败:`, error.message)
    if (hookConfig.name === 'pre-commit') {
      console.warn('  可手动运行: cd client/miniapp && npm run lint:staged')
    } else if (hookConfig.name === 'commit-msg') {
      console.warn('  可手动运行: cd client/miniapp && npx commitlint --edit')
    }
  }
}

/**
 * 卸载单个 hook：移除 miniapp 检查块，保留其他内容
 */
function uninstallHook(hookConfig) {
  const hookFile = nodePath.join(hooksDir, hookConfig.name)

  if (!fs.existsSync(hookFile)) {
    console.log(`${hookConfig.name} hook 不存在，无需清理`)
    return
  }

  try {
    const existing = fs.readFileSync(hookFile, 'utf-8')
    if (!existing.includes(hookConfig.marker)) {
      console.log(`${hookConfig.name} hook 未包含 miniapp 检查，无需清理`)
      return
    }

    // 移除 miniapp 检查块：从 MARKER 行开始，到下一个非 miniapp 内容或文件结尾
    const lines = existing.split('\n')
    const newLines = []
    let skipMode = false
    for (const line of lines) {
      if (line.includes(hookConfig.marker)) {
        skipMode = true
        continue
      }
      if (skipMode) {
        // 跳过 miniapp hook 块的后续行，直到遇到非 miniapp 内容
        if (line.startsWith('#!/bin/sh') && newLines.length === 0) {
          // 保留原 shebang
          newLines.push(line)
          skipMode = false
          continue
        }
        if (line.trim() === '' && newLines.length > 0 && newLines[newLines.length - 1].trim() !== '') {
          // 跳过 miniapp 块后的空行
          skipMode = false
          continue
        }
        if (line.startsWith('#!/') || (line.length > 0 && !line.startsWith('#') && !line.startsWith('STAGED') && !line.startsWith('if ') && !line.startsWith('  ') && !line.startsWith('fi') && !line.startsWith('echo'))) {
          // 遇到非 miniapp 内容，停止跳过
          skipMode = false
          newLines.push(line)
          continue
        }
        // 跳过 miniapp hook 块的行
        continue
      }
      newLines.push(line)
    }

    const newContent = newLines.join('\n').trimEnd() + '\n'
    if (newContent.trim() === '' || newContent.trim() === '#!/bin/sh') {
      // 如果清理后只剩 shebang 或空，删除整个文件
      fs.unlinkSync(hookFile)
      console.log(`✓ 已移除 miniapp ${hookConfig.name} hook（原 hook 仅含 miniapp 内容，已删除）`)
    } else {
      fs.writeFileSync(hookFile, newContent, { mode: 0o755 })
      fs.chmodSync(hookFile, 0o755)
      console.log(`✓ 已移除 miniapp 检查，保留原有 ${hookConfig.name} hook 内容`)
    }
  } catch (error) {
    console.warn(`⚠ 清理 ${hookConfig.name} hook 失败:`, error.message)
  }
}

function installAll() {
  ensureHooksDir()
  for (const hookConfig of HOOKS) {
    installHook(hookConfig)
  }
}

function uninstallAll() {
  for (const hookConfig of HOOKS) {
    uninstallHook(hookConfig)
  }
}

// 命令行参数：uninstall 时清理所有 hook
const action = process.argv[2]
if (action === 'uninstall') {
  uninstallAll()
} else {
  installAll()
}
