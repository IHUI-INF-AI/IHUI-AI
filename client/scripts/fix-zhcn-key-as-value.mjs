/**
 * 应用翻译表到 zh-CN JSON 文件
 * @param {string} filePath
 * @returns {{ modified: number, total: number, keyAsValueRemaining: number }}
 */
function processFile(filePath) {
  const raw = readFileSync(filePath, 'utf8')
  let parsed
  try {
    parsed = JSON.parse(raw)
  } catch (e) {
    console.error(`[ERROR] Failed to parse JSON: ${filePath}`, e.message)
    return { modified: 0, total: 0, keyAsValueRemaining: 0 }
  }

  let modified = 0
  let total = 0
  let keyAsValueRemaining = 0

  function walk(obj) {
    if (!obj || typeof obj !== 'object') return
    for (const k of Object.keys(obj)) {
      const v = obj[k]
      if (typeof v === 'string') {
        total++
        // 修复 key-as-value
        if (v === k) {
          const translated = TRANSLATION_TABLE[k]
          if (translated) {
            obj[k] = translated
            modified++
          } else {
            // 翻译表未覆盖, 保留键名 (vue-i18n fallback 比 "[ZH:key]" 占位符干净)
            keyAsValueRemaining++
          }
        }
      } else if (typeof v === 'object' && v !== null) {
        walk(v)
      }
    }
  }

  walk(parsed)

  if (modified > 0 && !DRY_RUN) {
    const formatted = JSON.stringify(parsed, null, 2) + '\n'
    writeFileSync(filePath, formatted, 'utf8')
  }

  return { modified, total, keyAsValueRemaining }
}

function main() {
  console.log('========================================')
  console.log('zh-CN key-as-value 修复脚本 (方案 B)')
  console.log('========================================')
  console.log(`LOCALE_DIR: ${LOCALES_DIR}`)
  console.log(`DRY_RUN: ${DRY_RUN}`)
  console.log(`Translation table size: ${Object.keys(TRANSLATION_TABLE).length}`)
  console.log('----------------------------------------')

  if (!statSync(LOCALES_DIR, { throwIfNoEntry: false })) {
    console.error(`[FATAL] Directory not found: ${LOCALES_DIR}`)
    process.exit(1)
  }

  const files = readdirSync(LOCALES_DIR).filter(f => f.endsWith('.json'))
  console.log(`Found ${files.length} zh-CN JSON files`)

  let totalModified = 0
  let totalKeyAsValue = 0
  const changedFiles = []

  for (const f of files) {
    const fp = join(LOCALES_DIR, f)
    const r = processFile(fp)
    if (r.modified > 0) {
      changedFiles.push({ file: f, modified: r.modified, remaining: r.keyAsValueRemaining })
    }
    totalModified += r.modified
    totalKeyAsValue += r.keyAsValueRemaining
  }

  changedFiles.sort((a, b) => b.modified - a.modified)

  console.log('\n========= Top changed files =========')
  for (const cf of changedFiles.slice(0, 20)) {
    console.log(`  ${cf.file.padEnd(30)} | modified: ${String(cf.modified).padStart(4)} | keyAsValue remaining: ${cf.remaining}`)
  }
  if (changedFiles.length > 20) {
    console.log(`  ... and ${changedFiles.length - 20} more files`)
  }

  console.log('\n========= Summary =========')
  console.log(`Total files changed:    ${changedFiles.length}`)
  console.log(`Total keys translated:  ${totalModified}`)
  console.log(`KeyAsValue remaining:   ${totalKeyAsValue} (kept as key fallback, will render as i18n key name)`)
  if (DRY_RUN) {
    console.log('\n[DRY-RUN] No files were modified. Re-run without --dry-run to apply changes.')
  } else {
    console.log('\n[OK] All changes written.')
  }
}

