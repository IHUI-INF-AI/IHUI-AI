/** 记忆作用域(统一三端:CLI/api/ai-service) */
export type MemoryScope = 'global' | 'project' | 'session' | 'user'

/** 记忆条目类型 */
export type MemoryEntryType =
  | 'preference' // 用户偏好
  | 'convention' // 项目约定
  | 'decision' // 历史决策
  | 'fact' // 事实信息
  | 'feedback' // 用户反馈
  | 'skill_ref' // skill 引用(指向 SkillDefinition)

/** 统一记忆条目(跨端共享) */
export interface MemoryEntry {
  /** 条目 ID(自增或 UUID) */
  id: string
  /** 作用域 */
  scope: MemoryScope
  /** 条目类型 */
  type: MemoryEntryType
  /** 分类(如 "UI 偏好"/"API 约定",缺省 "未分类") */
  category: string
  /** 条目文本 */
  text: string
  /** 来源端(cli/api/ai-service) */
  source: string
  /** 创建时间(ISO) */
  createdAt: string
  /** 更新时间(ISO) */
  updatedAt: string
}

/** 统一记忆读写请求 */
export interface MemorySyncRequest {
  /** 用户 ID(必填,隔离不同用户记忆) */
  userId: string
  /** 作用域(缺省 session) */
  scope?: MemoryScope
  /** 会话 ID(scope=session 时必填) */
  sessionId?: string
  /** 项目标识(scope=project 时必填,如 "IHUI-AI-<hash8>") */
  projectKey?: string
}

/** 统一记忆读写响应 */
export interface MemorySyncResponse {
  entries: MemoryEntry[]
  total: number
}
